package Cupcake;

public class Bottom {

    int bno;
    String name;
    float price;

    public Bottom(int bno, String name, float price) {
        this.bno = bno;
        this.name = name;
        this.price = price;
    }

    public int getBno() {
        return bno;
    }

    public String getName() {
        return name;
    }

    public float getPrice() {
        return price;
    }
}
